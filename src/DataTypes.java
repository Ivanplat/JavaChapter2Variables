
public class DataTypes {
	public static void methode() {
		boolean isInt = true;
		char ch = 's';
		byte x = 10;
		short xs = 20;
		int x1 = 30;
		long lx = 2147483648L;
		float f = 127.5F;
		double d = 127.5;
		System.out.println(isInt);
		System.out.println(ch);
		System.out.println(x);
		System.out.println(xs);
		System.out.println(x1);
		System.out.println(lx);
		System.out.println(f);
		System.out.println(d);
		
		System.out.println("Byte min-max values");
		System.out.println(Byte.MIN_VALUE);
		System.out.println(Byte.MAX_VALUE);
		System.out.println("");
		
		System.out.println("Integer min-max values");
		System.out.println(Integer.MIN_VALUE);
		System.out.println(Integer.MAX_VALUE);
		System.out.println("");
		
		System.out.println("Short min-max values");
		System.out.println(Short.MIN_VALUE);
		System.out.println(Short.MAX_VALUE);
		System.out.println("");
		
		System.out.println("Long min-max values");
		System.out.println(Long.MIN_VALUE);
		System.out.println(Long.MAX_VALUE);
		System.out.println("");
		
		System.out.println("Float min-max values");
		System.out.println(Float.MIN_VALUE);
		System.out.println(Float.MAX_VALUE);
		System.out.println("");
		
		System.out.println("Double min-max values");
		System.out.println(Double.MIN_VALUE);
		System.out.println(Double.MAX_VALUE);
		System.out.println("");
	}
}
