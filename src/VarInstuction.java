public class VarInstuction {
	public static void methode() {
		var a = true;
		System.out.println(a);
		var ch = 'w';
		System.out.println(ch);
		var x = 10;
		System.out.println(x);
		var y = 10.5;
		System.out.println(y);
		var s = "string";
		System.out.println(s);
		
		for(var i = 0; i < 10; i++) {
			System.out.println(i);
		}
		
		//var arr = new ArrayList<Integer>(); var can be used to declare these types
	}
}
